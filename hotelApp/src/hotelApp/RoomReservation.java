package hotelApp;

public class RoomReservation {
	
	int herbergisTeg;
	Hotel hotel;
	Guest guest;
	
	public RoomReservation(Hotel h, Guest g, int t) {
		hotel = h;
		guest = g;
		herbergisTeg = t;
	}

	public int daysBooked() {
		
		return daysBooked();
	}
	
	public static String guestBooked() {
		
		return guestBooked();
	}
	
	public Hotel getHotel() {
		return hotel;
	}
	
	public Guest getGuest() {
		return guest;
	}
	
	public int getHerbergisTeg() {
		return herbergisTeg;
	}
	
	

}
