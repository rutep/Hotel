package hotelApp;

public class RoomReservation {
	
	Hotel hotel;
	Guest guest;
	
	public RoomReservation(Hotel h, Guest g) {
		hotel = h;
		guest = g;
	}

	public int daysBooked() {
		
		return daysBooked();
	}
	
	public static String guestBooked() {
		
		return guestBooked();
	}
	
	public Hotel getHotel() {
		return hotel;
	}
	
	public Guest getGuest() {
		return guest;
	}

}
